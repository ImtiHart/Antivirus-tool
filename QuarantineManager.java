    private void ButUndoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButUndoActionPerformed
        int[] selectedRows = TableQuarantine.getSelectedRows();

        if (selectedRows.length == 0) {
            JOptionPane.showMessageDialog(this, "Please select one or more quarantined files to restore.");
            return;
        }

        DefaultTableModel model = (DefaultTableModel) TableQuarantine.getModel();
        int restoredCount = 0;

        for (int row : selectedRows) {
            String fileName = (String) model.getValueAt(row, 0);
            String quarantinePath = (String) model.getValueAt(row, 1);
            String originalPath = (String) model.getValueAt(row, 2);

            File quarantinedFile = new File(quarantinePath);
            File restoreTo = new File(originalPath);

            try {
                // Ensure the parent folder exists before restoring
                File parentDir = restoreTo.getParentFile();
                if (parentDir != null && !parentDir.exists()) {
                    parentDir.mkdirs();
                }

                // Move the file back to its original location
                Files.move(quarantinedFile.toPath(), restoreTo.toPath(), StandardCopyOption.REPLACE_EXISTING);

                // Update the status column to "Restored"
                model.setValueAt("Restored", row, 3);
                
                File metaF = new File(quarantinePath + ".meta");
                    //System.out.println(metaF.);
                    if (metaF.exists()){
                        metaF.delete();
                    }
                    
                restoredCount++;

            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Failed to restore " + fileName + ": " + e.getMessage());
            }
        }

        JOptionPane.showMessageDialog(this, restoredCount + " file(s) restored successfully.");

        // Optionally, remove restored rows (reverse order to avoid index shifting)
        for (int i = selectedRows.length - 1; i >= 0; i--) {
            model.removeRow(selectedRows[i]);
        }

        ButUndo.setEnabled(false);
        updateSummary();


    }//GEN-LAST:event_ButUndoActionPerformed


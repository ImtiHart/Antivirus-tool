    private void butDelAllMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_butDelAllMouseClicked
        DefaultTableModel model = (DefaultTableModel) TableQuarantine.getModel();

        if (model.getRowCount() == 0) {
            JOptionPane.showMessageDialog(this, "There are no quarantined files to delete.");
            return;
        }

        int confirm = JOptionPane.showConfirmDialog(
            this,
            "Are you sure you want to permanently delete all quarantined files?",
            "Confirm Deletion",
            JOptionPane.YES_NO_OPTION
        );

        if (confirm != JOptionPane.YES_OPTION) {
            return;
        }

        int deletedCount = 0;

        for (int i = 0; i < model.getRowCount(); i++) {
            String quarantinePath = (String) model.getValueAt(i, 1); // column 1 is the quarantine path
            File quarantinedFile = new File(quarantinePath);

            if (quarantinedFile.exists()) {
                if (quarantinedFile.delete()) {
                    File metaF = new File(quarantinePath + ".meta");
                    
                    if (metaF.exists()){
                        metaF.delete();
                    }
                    
                    deletedCount++;
                } else {
                    System.err.println("Failed to delete: " + quarantinedFile.getAbsolutePath());
                }
            }
        }

        // Clear the table after deleting
        model.setRowCount(0);

        JOptionPane.showMessageDialog(this, deletedCount + " quarantined file(s) deleted successfully.");

        updateSummary();
    }//GEN-LAST:event_butDelAllMouseClicked
     
    

    private void ButDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButDeleteActionPerformed
        // TODO add your handling code here:
        int[] selectedRows = TableQuarantine.getSelectedRows();

        if (selectedRows.length == 0) {
            JOptionPane.showMessageDialog(this, "Please select one or more quarantined files to delete.");
            return;
        }

        int confirm = JOptionPane.showConfirmDialog(
            this,
            "Are you sure you want to permanently delete the selected quarantined files?\nThis action cannot be undone.",
            "Confirm Deletion",
            JOptionPane.YES_NO_OPTION
        );

        if (confirm != JOptionPane.YES_OPTION) {
            return;
        }

        DefaultTableModel model = (DefaultTableModel) TableQuarantine.getModel();
        int deletedCount = 0;

        // Delete selected files
        for (int i = selectedRows.length - 1; i >= 0; i--) {  // iterate in reverse to avoid shifting
            int row = selectedRows[i];
            String quarantinePath = (String) model.getValueAt(row, 1);
            File quarantinedFile = new File(quarantinePath);
            
            if (quarantinedFile.exists()) {
                
                if (quarantinedFile.delete()) {
                    File metaF = new File(quarantinePath + ".meta");
                    //System.out.println(metaF.);
                    if (metaF.exists()){
                        metaF.delete();
                    }
                    deletedCount++;
                } else {
                    System.err.println("Failed to delete: " + quarantinedFile.getAbsolutePath());
                }
            }

            // Remove from the table
            model.removeRow(row);
        }

        JOptionPane.showMessageDialog(this, deletedCount + " quarantined file(s) permanently deleted.");

        updateSummary();

    }//GEN-LAST:event_ButDeleteActionPerformed


    private void loadExistingQuarantineFiles() {
        try {
            String quarantineFolder = System.getProperty("user.home") + File.separator + "Documents" + File.separator + "Quarantine";
            File folder = new File(quarantineFolder);
            if (!folder.exists()) return;

            File[] files = folder.listFiles((dir, name) -> name.endsWith(".quarantined"));
            if (files == null) return;

            for (File quarantinedFile : files) {
                String fileName = quarantinedFile.getName().replace(".quarantined", "");
                String originalPath = "Unknown";

                // Look for .meta file
                File metaFile = new File(quarantinedFile.getAbsolutePath() + ".meta");
                if (metaFile.exists()) {
                    try (BufferedReader reader = new BufferedReader(new FileReader(metaFile))) {
                        String line;
                        while ((line = reader.readLine()) != null) {
                            if (line.startsWith("originalPath=")) {
                                originalPath = line.substring("originalPath=".length());
                                break;
                            }
                        }
                    }
                }

                tableModel.addRow(new Object[]{fileName, quarantinedFile.getAbsolutePath(), originalPath, "Quarantined"});
            }

            updateSummary();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    

    /**
     * Add a threat file to the quarantine table
     */
    public void addThreatFile(String fileName, String originalPath) {
        if (autoSetting){
            try {
                String quarantineFolder = System.getProperty("user.home") + File.separator + "Documents" + File.separator + "Quarantine";
                Files.createDirectories(Path.of(quarantineFolder));

                // Create a unique quarantined file name
                String safeFileName = fileName + "_" + System.currentTimeMillis() + ".quarantined";
                File quarantineFile = new File(quarantineFolder, safeFileName);

                // Move the file
                File originalFile = new File(originalPath);
                Files.move(originalFile.toPath(), quarantineFile.toPath(), StandardCopyOption.REPLACE_EXISTING);

                // Create metadata file
                File metaFile = new File(quarantineFolder, safeFileName + ".meta");
                try (FileWriter writer = new FileWriter(metaFile)) {
                    writer.write("originalPath=" + originalPath + "\n");
                    writer.write("date=" + java.time.LocalDateTime.now());
                }

                // Add to table
                tableModel.addRow(new Object[]{fileName, quarantineFile.getAbsolutePath(), originalPath, "Quarantined"});
                totalThreats++;
                updateSummary();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Failed to quarantine file: " + e.getMessage());
                e.printStackTrace();
            }
        } else {
            tableModel.addRow(new Object[]{fileName," ",originalPath, "Not Quarantined"});
            totalThreats++;
            updateSummary();
        }
    }

